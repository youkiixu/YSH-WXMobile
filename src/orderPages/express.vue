<template >
<div class="container">
  <div class='express-header'>
    <div class="left">
      <div class="txt">订单编号：{{orderInfo.Id}}</div>
      <div class="txt">物流单号：{{expressInfo.oid}}</div>
      <div class="txt">物流公司：{{orderInfo.Remindtype != 0 ? '印捷物流' : '商家直邮'}}</div>
      <div class="txt">配送状态：{{expressInfo.strStatus}}</div>
    </div>
    <div class='right' v-if="expressInfo.isFinish === 1">
      <div class='update-btn' @click="updateExpress">更新物流</div>
    </div>
  </div>
  <div class='express-body'>
    <div class='current-icon'></div>
    <div :class="'express-item item-' + index" v-for="(item, index) of expressTraces" :key="index">
      <div class='left'></div>
      <div class='right'>
        <div class="info">{{item.Remark}}</div>
        <div class="time">{{item.strOper}}</div>
      </div>
    </div>
  </div>
</div>
</template>

<script>
import api from '@/utils/api'
import { mapState } from 'vuex'

export default {
  data () {
    return {
      orderInfo: {},
      expressInfo: {},
      expressTraces: []
    }
  },
  computed: {
    ...mapState(['userInfo'])
  },
  async mounted () {
    if (this.$route.query.data) {
      this.orderInfo = JSON.parse(this.$route.query.data)
    }
    await Promise.all([
      this.getExpressInfo()
    ])
    
  },
  methods: {
    // 获取物流信息
    getExpressInfo () {
      if(this.orderInfo.Remindtype != 0) {
        this.getYinJieExpress()
      } else {
        this.getDefaluExpress()
      }
    },
    // 查询印捷物流
    async getYinJieExpress () {
      var par = {
        UserId: this.userInfo.Id,
        // LogisticsOrderId: 1257322,
        OrderId: this.orderInfo.Id,
        source: 'wxmobile'
      }
      try {
        let res = await api.getYinJieExpress(par)
        if(typeof res == 'string') {
          res = JSON.parse(res)
        }
        if(res.result == 'ok') {
          var expressInfo = JSON.parse(res.orderInfo)
          var expressTraces = JSON.parse(res.orderStatus)
          this.expressInfo = expressInfo[0]
          this.expressTraces = expressTraces.reverse()
        } else {
          this.errorEvent()  
        }
      } catch (error) {
        this.errorEvent()
      }
    },
    // 查询商家直邮
    async getDefaluExpress() {
      var par = {
        ExpressCompanyName: this.orderInfo.ExpressCompanyName,
        ShipOrderNumber: this.orderInfo.ShipOrderNumber
      }
      const res = await getDefaluExpress(par)
      if(res.success) {
        this.expressInfo = res.data
      }
    },
    errorEvent () {
      this.$wx.showErrorToast('无物流信息')
      setTimeout(() => {
        this.$router.back()
      }, 1500);
    }
  }
}
</script>

